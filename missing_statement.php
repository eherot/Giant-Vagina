  <p class="style4" style="margin-top: 50px;">Admit it: it’s tough to project your fantasies onto these girls. All of the usual fetish objects are in place—plump lips, moist tongues, wild manes, slick plastic props—but something is missing. You might as well confess: had I completed these girls, you’d be lost in your erotic daydreams right now, either fantasizing about them, or about being them. Or perhaps you would overlook them entirely, erroneously assuming that the media has no affect upon your desires.</p>

  <p class="style4">Popular media sources such as Vogue and Disney are stunting the erotic range of women by churning out one-dimensional representations of female sexuality aimed at male and female consumers alike. This over-production speaks volumes about the media’s insidious role in our culture; its airbrush is definitely obscuring more than mere blemishes, cellulite and wrinkles. My work simply takes this marketing of female sexual identity to its logical extremes, reflecting what I see as the undue focus placed by both the media and consumers on expectant mouths, artificial hair, skimpy outfits, and eroticized innocence.</p>

  <p class="style4">I render my figures in great detail, using what is present to emphasize what I omit. Since my girls have their origins in the mass media, I interpret them in a glossy, Pop style. My backgrounds are inspired by Cecily Brown’s forceful and seductive handling of paint, and serve to firmly ground each figure outside of the flawless realm of popular culture. These backgrounds refer to the simpler pleasures of childhood - cotton candy, warm beaches, and cold glasses of milk. They are both luscious and a little violent - not unlike the figures within them - and the interplay between my girls and their backgrounds reinforces my feeling that neither is so sweet nor so simple.</p>  

  <p class="style4">“Missing” is creating a relationship between my subjects and my viewers utterly devoid of the enticing relationships created by advertising.  This is an often uncomfortable and intensely personal dialogue between voyeur and possible victim. Are you really aroused by women masquerading as young girls— powerless and infantilized?  Are you truly comfortable with pre-pubescent girls being portrayed as sexually precocious yet vulnerable?  I argue that we spend too much time ignoring our discomfort, but, hell, we certainly can't know for sure until we look with more critical eyes. You might as well admit it. But, in the end, you’ll have to make up your own mind.</p>

